package hub.sam.mof.xmi;

public class XmiException extends Exception {
    public XmiException() {
        super();
    }

    public XmiException(String message, Throwable cause) {
        super(message, cause);
    }

    public XmiException(String message) {
        super(message);
    }

    public XmiException(Throwable cause) {
        super(cause);
    } 
}
